<?php
    // Tipe Data String pada bahasa Pemrograman PHP, dapat menggunakan single quote ''

    echo 'Nama : ';
    echo 'Abimanyu Luthfi Rizq Ramadhan';

    // Selain menggunakan single quote, 
    // tipe data string juga bisa menggunakan double quote ""
    echo "Nama : ";
    echo "Abimanyu\t Luthfi\t Rizq\t Ramadhan";

    // Perintah dibawah ini akan tetap sama
    echo '\n';

    // Jika kita ingin mencetak syntax diatas menjadi breakline, maka harus menggunakan double quote.
    // Seperti program dibawah ini
    echo "\n";
?>